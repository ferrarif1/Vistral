const I18nContext = createContext<I18nContextValue | null>(null);

const resolveInitialLanguage = (): AppLanguage => {
  if (typeof window === 'undefined') {
    return 'zh-CN';
  }

  const saved = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (saved === 'zh-CN' || saved === 'en-US') {
    return saved;
  }

  return 'zh-CN';
};

const withVars = (template: string, vars?: TranslateVars): string =>
  template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_raw, key: string) => {
    if (!vars || !(key in vars)) {
      return `{${key}}`;
    }

    return String(vars[key]);
  });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(resolveInitialLanguage);

  useEffect(() => {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = useCallback((value: AppLanguage) => {
    setLanguageState(value);
  }, []);

  const t = useCallback(
    (source: string, vars?: TranslateVars): string => {
      const template = language === 'zh-CN' ? zhCN[source] ?? source : source;
      return withVars(template, vars);
    },
    [language]
  );

  const roleLabel = useCallback(
    (role?: UserRole | null) => {
      if (role === 'admin') {
        return t('Admin');
      }
      return t('User');
    },
    [t]
  );

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t,
      roleLabel
    }),
    [language, setLanguage, t, roleLabel]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

// eslint-disable-next-line react-refresh/only-export-components
export const useI18n = (): I18nContextValue => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
};
